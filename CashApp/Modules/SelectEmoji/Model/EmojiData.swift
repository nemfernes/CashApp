import Foundation

struct EmojiModel: Hashable {
    let title: String
    var isSelected: Bool = false

    func hash(into hasher: inout Hasher) {
        hasher.combine(title)
    }

    static func == (lhs: EmojiModel, rhs: EmojiModel) -> Bool {
        return lhs.title == rhs.title
    }
}


